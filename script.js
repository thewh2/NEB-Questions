// Lightbox Logic
function openLightbox(images, index) {
    isTransitioning = false;
    currentImages = images;
    currentIndex = index;

    // Initial load without slide-out
    const imgPath = currentImages[currentIndex];
    lightboxImg.src = imgPath;
    downloadBtn.href = imgPath;
    lightboxImg.className = 'flip-in-right'; // Initial pop in

    
    lightboxModal.style.display = 'flex';
    renderPagination();
    updateStarUI();
}

function updateStarUI() {
    const isFav = favorites.some(f => f.path === currentImages[currentIndex]);
    starBtn.classList.toggle('active', isFav);
}

starBtn.onclick = (e) => {
    e.stopPropagation();
    const imgPath = currentImages[currentIndex];
    const favIndex = favorites.findIndex(f => f.path === imgPath);

    if (favIndex > -1) {
        favorites.splice(favIndex, 1);
    } else {
        favorites.push({
            subject: currentSubject,
            year: currentYear,
            path: imgPath
        });
    }

    localStorage.setItem('neb_favs', JSON.stringify(favorites));
    updateStarUI();
    if (document.getElementById('favorites-grid').style.display !== 'none') {
        renderFavorites();
    }
};

async function downloadAsPDF(subject, year) {
    if (!window.jspdf) {
        alert("PDF Library not loaded yet. Please wait a moment.");
        return;
    }
    
    const { jsPDF } = window.jspdf;
    const doc = new jsPDF();
    loader.style.display = 'block';
    
    try {
        for (let i = 0; i < currentImages.length; i++) {
            const img = new Image();
            img.src = currentImages[i];
            
            await new Promise((resolve, reject) => {
                img.onload = resolve;
                img.onerror = () => reject(`Failed to load image: ${currentImages[i]}`);
            });

            if (i > 0) doc.addPage();
            
            const pageWidth = doc.internal.pageSize.getWidth();
            const pageHeight = doc.internal.pageSize.getHeight();
            const imgWidth = img.naturalWidth;
            const imgHeight = img.naturalHeight;
            const imgRatio = imgWidth / imgHeight;
            
            let w = pageWidth - 20; // Margin
            let h = w / imgRatio;
            
            if (h > pageHeight - 20) {
                h = pageHeight - 20;
                w = h * imgRatio;
            }
            
            const x = (pageWidth - w) / 2;
            const y = (pageHeight - h) / 2;
            
            doc.addImage(img, 'JPEG', x, y, w, h, undefined, 'FAST');
        }
        doc.save(`${subject}-${year}.pdf`);
    } catch (err) {
        console.error("PDF Error:", err);
        alert("PDF Generation Failed: " + (err.message || "Unknown Error"));
    } finally {
        loader.style.display = 'none';
    }
}

window.switchTab = (tab) => {
    if (tab === 'all') {
        dropdownTrigger.classList.add('active');
        favTabBtn.classList.remove('active');
        subjectGrid.style.display = 'grid';
        favoritesGrid.style.display = 'none';
        searchDiv.style.display = 'block';
        favActions.style.display = 'none';
    } else {
        dropdownTrigger.classList.remove('active');
        favTabBtn.classList.add('active');
        subjectGrid.style.display = 'none';
        favoritesGrid.style.display = 'grid';
        searchDiv.style.display = 'none';
        favActions.style.display = 'block';
        renderFavorites();
    }
}

window.filterBySubject = (selected, text) => {
    filterText.textContent = text;
    dropdownMenu.classList.remove('show');
    
    // Update active state in menu
    const items = document.querySelectorAll('.dropdown-item');
    items.forEach(item => {
        item.classList.toggle('active', item.textContent === text);
    });

    const cards = document.querySelectorAll('.subject-card');
    cards.forEach(card => {
        const sub = card.getAttribute('data-subject');
        if (selected === 'all' || sub === selected) {
            card.style.display = 'block';
        } else {
            card.style.display = 'none';
        }
    });

    // Reset search when filtering by subject
    searchInput.value = '';
    
    // Ensure home tab is active
    dropdownTrigger.classList.add('active');
    favTabBtn.classList.remove('active');
    subjectGrid.style.display = 'grid';
    favoritesGrid.style.display = 'none';
    searchDiv.style.display = 'block';
    favActions.style.display = 'none';
}


function renderPagination() {
    lightboxPagination.innerHTML = '';
    currentImages.forEach((_, index) => {
        const btn = document.createElement('div');
        btn.className = `page-num ${index === currentIndex ? 'active' : ''}`;
        btn.textContent = index + 1;
        btn.onclick = (e) => {
            e.stopPropagation();
            if (index !== currentIndex) {
                const direction = index > currentIndex ? 'next' : 'prev';
                currentIndex = index;
                updateLightbox(direction);
            }
        };
        lightboxPagination.appendChild(btn);
    });
}

function updateLightbox(direction = 'next') {
    if (isTransitioning) return;
    isTransitioning = true;

    // Clear inline styles from dragging
    lightboxImg.style.transform = '';
    lightboxImg.style.opacity = '';
    lightboxImg.style.transition = '';

    const outClass = direction === 'next' ? 'flip-out-left' : 'flip-out-right';
    const inClass = direction === 'next' ? 'flip-in-right' : 'flip-in-left';

    // Phase 1: Slide Out Current Image
    lightboxImg.className = outClass;

    setTimeout(() => {
        const imgPath = currentImages[currentIndex];

        // Show loader if image isn't cached
        loader.style.display = 'block';

        const tempImg = new Image();
        tempImg.onload = () => {
            loader.style.display = 'none';
            lightboxImg.src = imgPath;
            downloadBtn.href = imgPath;

            // Phase 2: Slide In New Image
            lightboxImg.className = inClass;
            renderPagination();
            updateStarUI();
            
            setTimeout(() => {
                isTransitioning = false;
            }, 600);
        };
        tempImg.src = imgPath;
    }, 300);
}

function nextImage() {
    if (isTransitioning) return;
    currentIndex = (currentIndex + 1) % currentImages.length;
    updateLightbox('next');
}

function prevImage() {
    if (isTransitioning) return;
    currentIndex = (currentIndex - 1 + currentImages.length) % currentImages.length;
    updateLightbox('prev');
}


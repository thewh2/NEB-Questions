// Interactive Touch Tracking
let touchStartX = 0;
let isDragging = false;
const swipeThreshold = window.innerWidth * 0.25;

lightboxModal.addEventListener('touchstart', (e) => {
    if (isTransitioning) return;
    touchStartX = e.touches[0].clientX;
    isDragging = true;
    lightboxImg.style.transition = 'none'; // Disable transition for real-time tracking
}, { passive: true });

lightboxModal.addEventListener('touchmove', (e) => {
    if (!isDragging || isTransitioning) return;

    const currentX = e.touches[0].clientX;
    const deltaX = currentX - touchStartX;
    const rotation = (deltaX / window.innerWidth) * -60; // Max 60deg rotation
    const scale = 1 - (Math.abs(deltaX) / window.innerWidth) * 0.2;

    lightboxImg.style.transform = `translateX(${deltaX}px) rotateY(${rotation}deg) scale(${scale})`;
    lightboxImg.style.opacity = 1 - (Math.abs(deltaX) / window.innerWidth) * 0.5;
}, { passive: true });

lightboxModal.addEventListener('touchend', (e) => {
    if (!isDragging || isTransitioning) return;
    isDragging = false;

    const touchEndX = e.changedTouches[0].clientX;
    const deltaX = touchEndX - touchStartX;

    lightboxImg.style.transition = 'transform 0.5s cubic-bezier(0.19, 1, 0.22, 1), opacity 0.4s ease';

    if (Math.abs(deltaX) > swipeThreshold) {
        if (deltaX < 0) {
            nextImage();
        } else {
            prevImage();
        }
    } else {
        // Snap Back
        lightboxImg.style.transform = 'translateX(0) rotateY(0) scale(1)';
        lightboxImg.style.opacity = '1';
    }
}, { passive: true });

// Deep Search Functionality
const searchInput = document.getElementById('subject-search');
searchInput.addEventListener('input', (e) => {
    const term = e.target.value.toLowerCase().trim();

    for (const [subject, years] of Object.entries(questionData)) {
        let subjectVisible = false;
        const matchingYears = [];

        // Determine if subject should be visible and which years match
        for (const [year, files] of Object.entries(years)) {
            const dualYear = getDualYear(year).toLowerCase();
            const yearMatch = year.includes(term) || dualYear.includes(term);
            const fileMatch = files.some(f => f.toLowerCase().includes(term));
            const subjectMatch = subject.toLowerCase().includes(term);

            if (subjectMatch || yearMatch || fileMatch) {
                subjectVisible = true;
                if (term === '' || yearMatch || fileMatch || subjectMatch) {
                    matchingYears.push(year);
                }
            }
        }

        // Find the card element for this subject
        const card = document.querySelector(`.subject-card[data-subject="${subject}"]`);
        if (card) {
            card.style.display = subjectVisible ? 'block' : 'none';

            // Filter individual year tags inside the card
            const tags = card.querySelectorAll('.year-tag');
            tags.forEach(tag => {
                const tagYear = tag.getAttribute('data-year');
                // If subject matches exactly, show all tags, otherwise filter tags
                if (term === '' || subject.toLowerCase().includes(term)) {
                    tag.style.display = 'inline-block';
                } else {
                    tag.style.display = matchingYears.includes(tagYear) ? 'inline-block' : 'none';
                }
            });
        }
    }
});


function getDualYear(adYear) {
    const yearNum = parseInt(adYear);
    const bsYear = yearNum + 57;
    return `${yearNum} (${bsYear} BS)`;
}

function renderSubjects() {
    subjectGrid.innerHTML = '';

    for (const [subject, years] of Object.entries(questionData)) {
        const card = document.createElement('div');
        card.className = 'subject-card';
        card.setAttribute('data-subject', subject);

        const yearTags = Object.keys(years).map(year =>
            `<span class="year-tag" data-year="${year}" onclick="openGallery('${subject}', '${year}')">${year}</span>`
        ).join('');

        card.innerHTML = `
            <div class="subject-name">
                <span class="icon">📘</span>
                ${subject}
            </div>
            <div class="year-list">
                ${yearTags}
            </div>
        `;

        subjectGrid.appendChild(card);
    }
    
    // Populate Custom Dropdown
    dropdownMenu.innerHTML = '<div class="dropdown-item active" onclick="filterBySubject(\'all\', \'All Subjects\')">All Subjects</div>';
    Object.keys(questionData).sort().forEach(sub => {
        const item = document.createElement('div');
        item.className = 'dropdown-item';
        item.textContent = sub;
        item.onclick = () => filterBySubject(sub, sub);
        dropdownMenu.appendChild(item);
    });
}

